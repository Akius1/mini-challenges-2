Object.freeze(MORSE_CODE);

function morse(text) { 
  if (typeof text === 'string' || text instanceof String){
    let chars = (text).split('  ').map((w) => w.split(' '));
    let temp = [];
    for(let i = 0; i < chars.length; i++){
      temp[i] = [];
      for(let x = 0; x < chars[i].length; x++){
        if(MORSE_CODE[chars[i][x]]){
            temp[i].push( MORSE_CODE[chars[i][x]] );
          }
      }
    }
    
    let decoded = temp.filter((item => item.length > 0));
    return decoded.map((arr, i) => arr.join('')).join(' ')

  }else {
    throw "Please provide a morse string"
  }
}
module.exports = morse;
